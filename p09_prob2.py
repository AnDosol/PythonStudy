import turtle # 모듈

t=turtle.Turtle()

t.shape("turtle") # arrow, circle, square, triangle

t.forward(70) # 방향
t.left(90) # 각도
t.forward(70)
t.left(90) # 각도
t.forward(70)
t.left(90) # 각도
t.forward(70)




turtle.mainloop()
turtle.bye()